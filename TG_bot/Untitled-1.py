# Структура данных: словарь, где ключ — это ID чата, а значение — множество пользователей (ID)
user_ids_dict = {
    12345: {1, 2, 3},  # chat_id: set of user_ids
    67890: {4, 5, 6},
}

# ID чата, из которого нужно удалить пользователя
chat_id = 12345
# ID пользователя, которого нужно удалить
user_id = 2

# Удаление пользователя из множества пользователей чата
if chat_id in user_ids_dict:
    user_ids_dict[chat_id].discard(user_id)  # Удаляем ID из множества (discard не вызывает ошибку, если ID нет)

# Проверим результат
print(user_ids_dict)
